'use strict';

let numberOfFilms;

function start() {
    numberOfFilms = +prompt('Сколько фильмов вы уже посмотрели?', '');

    while (numberOfFilms == '' || numberOfFilms == null || isNaN(numberOfFilms)) {
        numberOfFilms = +prompt('Сколько фильмов вы уже посмотрели?', '');
    }
}
start();

const personalMovieDB = {
    count: numberOfFilms,
    movies: {},
    actors: {},
    genres: [],
    privat: false
};

function rememberMyFilms() {
    for (let i = 0; i < 2; i++) {
        const nameMovie = prompt('Один из последних просмотренных фильмов?', ''),
            rangeMovie = prompt('На сколько оцените его?', '');

        if (nameMovie != null && nameMovie.length < 50 && nameMovie.length > 0
            && rangeMovie != null && rangeMovie.length < 50 && rangeMovie.length > 0) {
            personalMovieDB.movies[nameMovie] = rangeMovie;
        } else {
            i--;
        }
    }
}
rememberMyFilms();

function detectPersonalLevel() {
    if (personalMovieDB.count < 10) {
        alert("Просмотрено довольно мало фильмов");
    } else if (personalMovieDB.count >= 10 && personalMovieDB.count < 30) {
        alert("Вы классический зритель");
    } else if (personalMovieDB.count >= 30) {
        alert("Да вы киноман!");
    } else {
        alert("Произошла ошибка");
    }
}
detectPersonalLevel();

function showMyDB(hidden) {
    if (!hidden) {
        console.log(personalMovieDB);
    }
}
showMyDB(personalMovieDB.privat);

function writeYourGenres() {
    for (let n = 1; n < 4; n++) {
        personalMovieDB.genres[n - 1] = prompt(`Ваш любимый жанр под номером ${n}?`);
    }
}
writeYourGenres();

